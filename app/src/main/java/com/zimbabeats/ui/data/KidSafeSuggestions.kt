    /**
     * Filter suggestions based on query with smart matching
     * - Shows prefix matches first (suggestions starting with query)
     * - Then shows contains matches
     * Returns matching suggestions that contain the query (case-insensitive)
     */
    fun filterSuggestions(query: String, isMusic: Boolean): List<String> {
        val suggestions = if (isMusic) musicSuggestions else videoSuggestions
        val trimmedQuery = query.trim()

        if (trimmedQuery.isBlank()) {
            // Show popular suggestions when no query
            return suggestions.take(12)
        }

        // Smart filtering: prioritize prefix matches, then contains matches
        val prefixMatches = suggestions.filter {
            it.startsWith(trimmedQuery, ignoreCase = true)
        }

        val containsMatches = suggestions.filter {
            it.contains(trimmedQuery, ignoreCase = true) &&
            !it.startsWith(trimmedQuery, ignoreCase = true)
        }

        // Combine: prefix matches first, then contains matches
        return (prefixMatches + containsMatches).take(12)
    }

    /**
     * Get suggestions for a specific character/show
     */
    fun getCharacterSuggestions(character: String, isMusic: Boolean): List<String> {
        return filterSuggestions(character, isMusic)
    }
